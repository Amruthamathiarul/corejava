package PrimitiveDataTypes;

public class Example {

	public static void main(String[] args) {
    //boolean Example
		boolean IoT=true;
		System.out.println(IoT);
	// byte example
		byte range;
		range=122;
		System.out.println(range);
	//short example
		short range1;
		range1=122;
		System.out.println(range1);
	// int example
		int range2;
		range2=500000000;
		System.out.println(range2);
	//long example
		long number=650000000000l;
		System.out.println(number);
	//float example
		float number1=95.5f;
		System.out.println(number1);
	//char example(unicode value of A is '/u0031')
		char letter='\u0031';
		System.out.println(letter);
	//char usingASCII
		char letter1=46;//ASCII value of A is 46
		System.out.println(letter1);
	char letter3='A';
	System.out.println(letter3);
		
		
	}	
		
	
	}
